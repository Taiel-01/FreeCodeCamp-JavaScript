//* .push() - Empuja al final del arreglo
var myArray = [["John", 23], ["cat", 2]];
myArray.push(["dog", 3])


//* .pop() - Elimina el ultimo elemento
var myArray = [["John", 23], ["cat", 2]];
myArray.pop();


//* .shift() - Elimina el primer elemento 
var myArray = [["John", 23], ["cat", 2]];
myArray.shift();


//* .unshift() - Agrega el primer elemento 
var myArray = [["John", 23], ["cat", 2]];
myArray.unshift(["Paul",35]);
